package com.mcheru.leaderboard.retrofitutyls;

public class Project {
     String mail;
     String fName;
     String lName;
     String link;

    public Project() {
    }

    public Project(String mail, String fName, String lName, String link) {
        this.mail = mail;
        this.fName = fName;
        this.lName = lName;
        this.link = link;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public String getfName() {
        return fName;
    }

    public void setfName(String fName) {
        this.fName = fName;
    }

    public String getlName() {
        return lName;
    }

    public void setlName(String lName) {
        this.lName = lName;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
}
